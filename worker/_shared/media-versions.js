/** R2 backup / staging / commit for before-after art compare. */
import { mediaUrl, r2MediaKey } from "./freemium-image.js";

export function assetFilename(kind, key) {
  if (kind === "cover") return "cover.png";
  if (kind === "characters") return `char_${key}.png`;
  if (kind === "backgrounds") return `bg_${String(key).replace(/[^\w-]+/g, "_")}.png`;
  if (kind === "inserts") return `insert_${key}.png`;
  return null;
}

export function prevFilename(kind, key) {
  const fn = assetFilename(kind, key);
  if (!fn) return null;
  const dot = fn.lastIndexOf(".");
  if (dot === -1) return `${fn}.prev`;
  return `${fn.slice(0, dot)}.prev${fn.slice(dot)}`;
}

export function stagingFilename(kind, key) {
  const fn = assetFilename(kind, key);
  if (!fn) return null;
  const dot = fn.lastIndexOf(".");
  if (dot === -1) return `${fn}.next`;
  return `${fn.slice(0, dot)}.next${fn.slice(dot)}`;
}

export function prevPublicUrl(bookId, style, kind, key) {
  const pfn = prevFilename(kind, key);
  return pfn ? mediaUrl(bookId, style, pfn) : null;
}

export function stagingPublicUrl(bookId, style, kind, key, bust) {
  const sfn = stagingFilename(kind, key);
  if (!sfn) return null;
  return mediaUrl(bookId, style, sfn, bust ? { bust } : {});
}

export function cacheBustUrl(url, ts = Date.now()) {
  if (!url) return url;
  const base = url.split("?")[0];
  return `${base}?v=${ts}`;
}

/** Copy live R2 object → `.prev` sibling; return its public URL if backup was made. */
export async function backupMediaAsset(env, bookId, style, kind, key) {
  if (!env.VAE_PACKS) return null;
  const fn = assetFilename(kind, key);
  const prevFn = prevFilename(kind, key);
  if (!fn || !prevFn) return null;
  const liveKey = r2MediaKey(bookId, style, fn);
  const obj = await env.VAE_PACKS.get(liveKey);
  if (!obj) return null;
  await env.VAE_PACKS.put(r2MediaKey(bookId, style, prevFn), await obj.arrayBuffer(), {
    httpMetadata: obj.httpMetadata || { contentType: "image/png" },
  });
  return mediaUrl(bookId, style, prevFn);
}

/** Write generated bytes to `.next` — live + playback stay unchanged until commit. */
export async function writeStagingAsset(env, bookId, style, kind, key, bytes, contentType) {
  const sfn = stagingFilename(kind, key);
  if (!sfn) return null;
  await env.VAE_PACKS.put(r2MediaKey(bookId, style, sfn), bytes, {
    httpMetadata: { contentType: contentType || "image/png" },
  });
  return mediaUrl(bookId, style, sfn);
}

/** Promote `.next` → live, drop `.prev`, return cache-busted live URL. */
export async function commitStagingAsset(env, bookId, style, kind, key) {
  if (!env.VAE_PACKS) return null;
  const fn = assetFilename(kind, key);
  const sfn = stagingFilename(kind, key);
  const prevFn = prevFilename(kind, key);
  if (!fn || !sfn) return null;
  const stagingObj = await env.VAE_PACKS.get(r2MediaKey(bookId, style, sfn));
  if (!stagingObj) {
    const live = await env.VAE_PACKS.get(r2MediaKey(bookId, style, fn));
    if (!live) return null;
    if (prevFn) await env.VAE_PACKS.delete(r2MediaKey(bookId, style, prevFn)).catch(() => {});
    return cacheBustUrl(mediaUrl(bookId, style, fn));
  }
  await env.VAE_PACKS.put(r2MediaKey(bookId, style, fn), await stagingObj.arrayBuffer(), {
    httpMetadata: stagingObj.httpMetadata || { contentType: "image/png" },
  });
  await env.VAE_PACKS.delete(r2MediaKey(bookId, style, sfn)).catch(() => {});
  if (prevFn) await env.VAE_PACKS.delete(r2MediaKey(bookId, style, prevFn)).catch(() => {});
  return cacheBustUrl(mediaUrl(bookId, style, fn));
}

/** Discard `.next` and restore `.prev` → live. */
export async function revertStagingAsset(env, bookId, style, kind, key) {
  if (!env.VAE_PACKS) return false;
  const fn = assetFilename(kind, key);
  const sfn = stagingFilename(kind, key);
  const prevFn = prevFilename(kind, key);
  if (!fn) return false;
  if (sfn) await env.VAE_PACKS.delete(r2MediaKey(bookId, style, sfn)).catch(() => {});
  const prevObj = await env.VAE_PACKS.get(r2MediaKey(bookId, style, prevFn));
  if (!prevObj) return false;
  await env.VAE_PACKS.put(r2MediaKey(bookId, style, fn), await prevObj.arrayBuffer(), {
    httpMetadata: prevObj.httpMetadata || { contentType: "image/png" },
  });
  await env.VAE_PACKS.delete(r2MediaKey(bookId, style, prevFn)).catch(() => {});
  return true;
}

/** @deprecated use commitStagingAsset */
export async function commitMediaAsset(env, bookId, style, kind, key) {
  return commitStagingAsset(env, bookId, style, kind, key);
}

/** @deprecated use revertStagingAsset */
export async function revertMediaAsset(env, bookId, style, kind, key) {
  const ok = await revertStagingAsset(env, bookId, style, kind, key);
  return ok;
}

function findPlaybackLineByIdx(playback, lineIdx) {
  const idx = Number(lineIdx);
  for (const scene of playback?.scenes || []) {
    for (const line of scene.lines || []) {
      if (line.idx === idx) return line;
    }
  }
  return null;
}

/** Patch playback JSON URLs after revert/commit. */
export async function patchPlaybackMediaUrl(env, bookId, kind, key, url) {
  const obj = await env.VAE_PACKS.get(`books/${bookId}.json`);
  if (!obj) return;
  const playback = await obj.json();
  if (kind === "characters" && playback.characters?.[key]) {
    playback.characters[key].sprite = url;
  }
  if (kind === "cover") playback.cover = url;
  if (kind === "backgrounds") {
    for (const scene of playback.scenes || []) {
      if (scene.id === key) scene.background = url;
    }
  }
  if (kind === "inserts") {
    playback.inserts = playback.inserts || {};
    playback.inserts[key] = url;
    const line = findPlaybackLineByIdx(playback, key);
    if (line) {
      line.illustration_url = url;
      if (!line.illustration_caption && line.text) {
        const cap = String(line.text).slice(0, 72).trim();
        line.illustration_caption = cap + (String(line.text).length > 72 ? "…" : "");
      }
      line.visual_moment = true;
    }
  }
  for (const scene of playback.scenes || []) {
    for (const p of scene.present || []) {
      if (kind === "characters" && p.character_id === key) p.sprite = url;
    }
  }
  await env.VAE_PACKS.put(`books/${bookId}.json`, JSON.stringify(playback, null, 2), {
    httpMetadata: { contentType: "application/json" },
  });
}
